a = "Hello World"       # Variable Declaration with string value.
print(a)

b = 10                  # Variable Declaration with integer value.
print(b)

c = 3.14                # Variable Declaration with float value.
print(c)

firstname = "Yash"      # Variable Declaration without using underscore.
print(firstname)

Last_Name = "Jadhav"    # Proper Variable Declaration using underscore.
print(Last_Name)

num1 = 5                # Variable Declaration with numeric value.
print(num1)

first_name = "Rahul"    # Variable Declaration using lowercase letters and underscore.
print(first_name)

First_name = "Aditya"   # Variable Declaration using uppercase letter at the beginning.
print(First_name)

a = b = c = 30          # Multiple Variable Declaration in a single line.
print(b)

a = b = c = 10, 50      # Multiple Variable Declaration with tuple values.
print(b)

a, b, c = 10, 30, 50    # Multiple Variable Declaration with different values.
print(b)
print(a)
print(c)